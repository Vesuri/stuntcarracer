_Start						;A0 = resident loader
;======================================================================
	lea	_resload(pc),a1
	move.l	a0,(a1)			;Save for later use

	move.l	a0,a2			;Check parameters
	lea	_Tags(pc),a0
	jsr	resload_Control(a2)

	lea	executable,a0
	move.l	_expmem(pc),a1
	move.l	a1,a5
	move.l	#executableSize/4-1,d1
.copy:	move.l	(a0)+,(a1)+
	dbra	d1,.copy

	move.l	a5,d0
	add.l	#expMemSize,d0
	move.l	d0,sp

	move.l	_resload(pc),a2
	move.l	a5,a0
	clr.l	-(sp)
	move.l	_ChipPtr(pc),-(sp)
	pea     WHDLTAG_CHIPPTR
	pea     -1
	pea     WHDLTAG_LOADSEG
	move.l  sp,a1
	jsr	resload_Relocate(a2)
	lea	5*4(sp),sp

	; Get code base address (first hunk address + 8)
	lea	8(a5),a4

	; Get gameData base address from a lea gameData,a0 instruction
	move.l	2(a4),a5

	; Store hook addresses and parameters
	move.l	a5,a3
	add.l	#gameDataSize,a3
	lea	_SaveTimes(pc),a0
	move.l	a0,(a3)+
	lea	_Loader(pc),a0
	move.l	a0,(a3)+
	lea	_Custom1(pc),a0
	move.l	(a0),(a3)+

	; Rob Northen requires one longword before the actual payload
	lea	-4(a5),a5

	; Load the encrypted game data 8192 bytes at a time
	move.l	a5,a3
	move.l	#$e898,d6
	move.l	#gameDataSize+4,d7
.readLoop:
	move.l	d6,d0
	move.l	d7,d1
	cmp.l	#$2000,d1
	bls.s	.sizeOk
	move.l	#$2000,d1
.sizeOk:
	moveq	#1,d2
	move.l	a3,a0
	lea	(a3,d1.w),a3
	add.l	d1,d6
	sub.l	d1,d7
	jsr	resload_DiskLoad(a2)
	tst.l	d7
	bne.s	.readLoop

	; Decrypt the game data
	move.l	#(gameDataSize+4)>>2,d0
	move.l	#$c905b365,d5
	move.l	#$a0cff27b,d6
	move.l	#$59f3a592,d7
	move.l	a5,a0
	bsr	_Decrypt
	jsr	resload_FlushCache(a2)

	; Apply TNT track data if Custom2 (The New Tracks) is enabled
	lea	_PL_TNTData(pc),a0
	move.l	a5,a1
	jsr	resload_Patch(a2)

	; Store save filesize
	lea     _SaveName(pc),a0
	jsr     resload_GetFileSize(a2)
	lea     _SaveFileSize(pc),a0
	move.l  d0,(a0)

	; Disable filter
	bset	#1,$bfe001

	; Start the game
	jsr	(a4)

	; Quit
	move.l	_resload,a2
	pea	TDREASON_OK
	jmp	resload_Abort(a2)

